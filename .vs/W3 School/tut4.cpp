#include <iostream>
using namespace std;

int main () {
    int a , b;

    cout << "Please enter the integers: " << endl;
    cin >> a;
    cin >> b;

    cout << "The addition is: " << a + b << endl;
    cout << "The substraction is: " << a - b << endl;
    cout << "The multipication is: " << a * b << endl;
    cout << "The division is: " << a / b << endl;
    cout << "The remainder is: " << a % b << endl; 
    return 0;
}
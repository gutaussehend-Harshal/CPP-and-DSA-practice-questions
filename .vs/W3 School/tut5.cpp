#include <iostream>
using namespace std;

int main () {
    int x = 5;
    int y = 3;

    cout << "The x is greater than y: " << (x > y) << endl;
    cout << "The x is less than y: " << (x < y) << endl;
    cout << "The x is equal to y: " << (x == y) << endl;
    cout << "The x is not equal to y: " << (x != y) << endl;
    cout << "The x is greater than or eqaul to y: " << (x >= y) << endl;
    cout << "The x is less than or equal to y: " << (x <= y) << endl; 

    return 0;
}